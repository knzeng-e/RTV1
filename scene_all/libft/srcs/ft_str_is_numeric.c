#include "libft.h"

int	ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	if (str[i] == '-')
		i++;
	while (str[i] != '\0')
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	if (i == 1)
		return (str[--i] != '-');
	return (1);
}
