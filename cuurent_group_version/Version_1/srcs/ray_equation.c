void	ray_equation(t_ray *ray)
{
	/*ray =  0 + DIR * t
	ray->origin.x = ray->t * (ray->origin.x + ray->direction.vect_x);
	ray->origin.y = ray->t * (ray->origin.y + ray->direction.vect_y);
	ray->origin.z = ray->t * (ray->origin.y + ray->direction.vect_z);*/
	/*to check : setting new direction */
	ray += 0;
}
